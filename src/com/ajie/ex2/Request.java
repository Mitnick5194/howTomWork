	/**
	 * 根据请求的信息字符串提取uri部分 分割的依据是http协议头的规则
	 * 
	 * http响应头部规则如下：<br>
	 * GET / HTTP/1.1 Host: 127.0.0.1:9999 Connection: keep-alive Cache-Control:
	 * max-age=0
	 * 
	 * @param requestString
	 * @return
	 */
	public String parseUri(String requestString) {
		int index1, index2;
		index1 = requestString.indexOf(" ");
		if (index1 != -1) {
			index2 = requestString.indexOf(" ", index1+1);
			if (index2 != -1)
				uri = requestString.substring(index1 + 1, index2);
		}

		return null;
	}

	/**
	 * 解析请求信息 并打印
	 */
	public void parse() {
		byte[] buf = new byte[BUFFER_SIZE];
		StringBuffer sb = new StringBuffer();
		int n = 0;
		do {
			try {
				n = input.available();
				input.read(buf, 0, Math.min(BUFFER_SIZE, n));
				sb.append(new String(buf));
			} catch (IOException e) {
				e.printStackTrace();
			}
		} while (n > BUFFER_SIZE);
		System.out.println(sb.toString());
		parseUri(sb.toString());
	}

	public String getUri() {
		return uri;
	}

